
const express = require('express');
const mongoose = require('mongoose');
const Role = require("../models/Role");
const routerRole = express.Router();

// Create
routerRole.post("/", async (req, res) => {
  try {
    const newCourse = await Role.create(req.body);
    return res.status(201).json(newCourse);
  } catch (error) {
    return res.status(500).json({ error: "Could not create course" });
  }
});

// Read all
routerRole.get("/", async (req, res) => {
  try {
    const courses = await Role.find();
    return res.status(200).json(courses);
  } catch (error) {
    return res.status(500).json({ error: "Could not retrieve courses" });
  }
});

// Read by ID
routerRole.get("/:id", async (req, res) => {
  try {
    const course = await Role.findById(req.params.id);
    if (!course) {
      return res.status(404).json({ error: "Course not found" });
    }
    return res.status(200).json(course);
  } catch (error) {
    return res.status(500).json({ error: "Could not retrieve course" });
  }
});

// Update
routerRole.put("/:id", async (req, res) => {
  try {
    const updatedCourse = await Role.findByIdAndUpdate(
      req.params.id,
      req.body,
      { new: true }
    );
    if (!updatedCourse) {
      return res.status(404).json({ error: "Course not found" });
    }
    return res.status(200).json(updatedCourse);
  } catch (error) {
    return res.status(500).json({ error: "Could not update course" });
  }
});

// Delete
routerRole.delete("/:id", async (req, res) => {
  try {
    const deletedCourse = await Role.findByIdAndDelete(req.params.id);
    if (!deletedCourse) {
      return res.status(404).json({ error: "Course not found" });
    }
    return res.status(204).json();
  } catch (error) {
    return res.status(500).json({ error: "Could not delete course" });
  }
});

module.exports = routerRole;
